<?php
namespace App\Model;

class VolumeSearch
{
    private int $id;

    private string $title = '';

    public function getId(): int
    {
        return $this->id;
    }

    public function getTitle(): string
    {
        return $this->title;
    }

    public function setTitle(string $title): self
    {
        $this->title = $title;

        return $this;
    }
}
